    public void loadReceipt(int billId) {
        // Clear previous data
        customername.setText("");
        customeraccountnumber.setText("");
        billid.setText("");
        paymentid.setText("");
        paymentdate.setText("");
        paymentmethod.setText("");
        amtpaid.setText("");

        try (Connection con = connectDB.getConnection()) {
            // Query to get payment and bill details for the receipt
            String query = "SELECT CONCAT(u.firstname, ' ', u.lastname) AS name, u.account_number, p.bill_id, p.payment_id, p.payment_date, p.payment_method, p.amount_paid " +
                            "FROM tbl_payment p " +
                            "JOIN tbl_bill b ON p.bill_id = b.b_id " +
                            "JOIN users u ON b.user_id = u.id " +
                            "WHERE p.bill_id = ? AND b.status = 'Paid'";


            PreparedStatement pst = con.prepareStatement(query);
            pst.setInt(1, billId);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                customername.setText(rs.getString("name"));
                customeraccountnumber.setText(rs.getString("account_number"));
                billid.setText(String.valueOf(rs.getInt("bill_id")));
                paymentid.setText(String.valueOf(rs.getInt("payment_id")));
                paymentdate.setText(rs.getString("payment_date"));
                paymentmethod.setText(rs.getString("payment_method"));
                amtpaid.setText(String.format("%.2f", rs.getDouble("amount_paid")));
            } else {
                JOptionPane.showMessageDialog(this, "No paid bill found with the selected ID.", "No Data", JOptionPane.WARNING_MESSAGE);
            }

            rs.close();
            pst.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading receipt: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

