    public void loadBill(int billId) {
        try (Connection con = config.connectDB.getConnection()) {
            String query = "SELECT b.b_id, u.account_number, b.bill_month, b.kwh_used, b.due_date, b.status " +
                           "FROM tbl_bill b " +
                           "JOIN users u ON b.user_id = u.id " +
                           "WHERE b.b_id = ?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setInt(1, billId);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                accountnumber.setText(rs.getString("account_number"));
                monthcombobox.setSelectedItem(rs.getString("bill_month"));
                kWh.setText(String.valueOf(rs.getInt("kwh_used")));
                duedate.setText(rs.getString("due_date"));
                // Store billId for update
                this.userId = billId;
            } else {
                JOptionPane.showMessageDialog(this, "Bill not found.", "Error", JOptionPane.ERROR_MESSAGE);
                this.dispose();
            }
            rs.close();
            pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading bill: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }

